function currentLang() {
  if (typeof localStorage === 'undefined') return 'zh';
  return localStorage.getItem('chatpulse_lang') === 'en' ? 'en' : 'zh';
}

export function pixelTx(en, zh, lang = currentLang()) {
  return lang === 'en' ? en : zh;
}

export function translatePixelText(value, lang = currentLang()) {
  if (lang !== 'en' || value === null || value === undefined) return value;
  let result = String(value);
  const compact = result.replace(/\s+/g, '');
  if (PIXEL_TEXT_EN[compact]) return PIXEL_TEXT_EN[compact];
  if (PIXEL_TEXT_EN[result]) return PIXEL_TEXT_EN[result];
  for (const [zh, en] of PIXEL_REPLACEMENTS) {
    if (result.includes(zh)) result = result.split(zh).join(en);
  }
  return result;
}

export function translatePixelAction(action, lang = currentLang()) {
  if (lang !== 'en' || !action) return action;
  return {
    ...action,
    label: translatePixelText(action.label, lang),
    hint: translatePixelText(action.hint, lang),
    description: translatePixelText(action.description, lang)
  };
}
